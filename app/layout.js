import './globals.css';
import { cookies } from 'next/headers';
import Link from 'next/link';
import { getUserBySessionToken } from '../database/users';
import BackButton from './BackButton';

export const dynamic = 'force-dynamic';

export const metadata = {
  title: 'Create Next App',
  description: 'Generated by create next app',
};
export default async function RootLayout({ children }) {
  const cookieStore = cookies();
  const sessionToken = cookieStore.get('sessionToken');

  const user = !sessionToken?.value
    ? undefined
    : await getUserBySessionToken(sessionToken.value);

  return (
    <html lang="en">
      <head />
      <body className="p-2">
        <div className="navbar bg-transparent rounded-box">
          <div className="flex-1 px-2 lg:flex-none">{/* <BackButton /> */}</div>
          <div className="flex justify-end flex-2 px-0">
            <div className="flex items-stretch">
              <div className="dropdown dropdown-end">
                <button tabIndex={0} className="btn btn-ghost rounded-btn">
                  <svg
                    xmlns="http://www.w3.org/2000/svg"
                    fill="none"
                    viewBox="0 0 24 24"
                    strokeWidth={1.5}
                    stroke="currentColor"
                    className="w-6 h-6"
                  >
                    <path
                      strokeLinecap="round"
                      strokeLinejoin="round"
                      d="M3.75 5.25h16.5m-16.5 4.5h16.5m-16.5 4.5h16.5m-16.5 4.5h16.5"
                    />
                  </svg>
                </button>
                <ul
                  tabIndex={0}
                  className="menu dropdown-content p-2 shadow bg-yellow rounded-box w-52 mt-4"
                >
                  <li>
                    <a href="/">HOME</a>
                  </li>
                  <li>
                    <a href="/about">ABOUT</a>
                  </li>

                  <li>
                    <a href="/events">EVENTS</a>
                  </li>
                  <li>
                    {user ? (
                      <>
                        <Link href={`/profile/${user.username}`}>
                          <div className="text-blue">
                            {user.username.toUpperCase()}
                          </div>
                        </Link>
                        <Link href="/logout" prefetch={false}>
                          LOGOUT
                        </Link>
                      </>
                    ) : (
                      <>
                        <Link href="/register">REGISTER</Link>
                        <Link href="/login">LOGIN</Link>
                      </>
                    )}
                  </li>
                </ul>
              </div>
            </div>
          </div>
        </div>
        {children}
      </body>
    </html>
  );
}
